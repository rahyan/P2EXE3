package P2EXE3;

public class Professor {
	
	private String rollNo;
	private String name;
	
	public String getProfessorRollNo() {
		return rollNo;
	}

	public void setProfessorRollNo(String rollNo) {
		this.rollNo = rollNo;
	}

	public String getProfessorName() {
		return name;
	}

	public void setProfessorName(String name) {
		this.name = name;
	}

}
