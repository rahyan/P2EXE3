package P2EXE3;

public class Student {
	
	private String rollNo;
	private String name;
	
	public String getStudentRollNo() {
		return rollNo;
	}

	public void setStudentRollNo(String rollNo) {
		this.rollNo = rollNo;
	}

	public String getStudentName() {
		return name;
	}

	public void setStudentName(String name) {
		this.name = name;
	}

}
